package src.baekCoding.category.strings;

import java.util.Scanner;

public class 그대로출력하기_11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()){
            String s = sc.nextLine();
            System.out.println(s);
        }
        sc.close();
    }
}
